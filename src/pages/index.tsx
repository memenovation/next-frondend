export async function getServerSideProps(context) {
  const session = await unstable_getServerSession(
    //@ts-ignore
    context.req,
    context.res,
    authOptions
  );

  if (!session) {
    return {
      redirect: {
        destination: "/auth/signin",
        permanent: false,
      },
    };
  }
  console.log("server session", session);

  return {
    props: {},
  };
}
